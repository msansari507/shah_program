package package001;

public class NestedIfElse {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		if(a<=b) {
			if(a==b) {
				System.out.println("1");
			}
			else {
				System.out.println("2");
			}
		}
		else {
			System.out.println("3");
		}
	}

}
