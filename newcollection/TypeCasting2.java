package newcollection;

public class TypeCasting2 {

	public static void main(String[] args) {
		// Assignment 113 WAP to convert short into byte
		short s = 9999;
		System.out.println("s in short type: "+s);
		byte g = (byte)s;
		System.out.println("g in byte type: "+g);
		// Assignment 114 WAP to convert short into int
		short h = 9999;
		System.out.println("h in short type: "+h);
		int k = (int)h;
		System.out.println("k in int type: "+k);
		// Assignment 115 WAP to convert float into double
		float e = 3.1467f;
		System.out.println("e in float type: "+e);
		double d = e;
		System.out.println("d in double type: "+d);
	}

}
